package com.gray.bean;

public class Article {
	private String name;
	private Float price;
	private int id;
	public String getName() {
		return name;
	} 
	public void setName(String name) {
		this.name = name;
	}
	public Float getPrice() {
		return price;
	}
	public void setPrice(Float price) {
		this.price = price;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
}
